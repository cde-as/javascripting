// Func doesn't take parameter
const sayHello = function() {
  console.log("hello, world");
};

sayHello()

//-----------------------------
// Func takes in a parameter

const sayHelloTwo = function (name) {
  console.log("Hello, " + name);
};

sayHelloTwo("Chris");
sayHelloTwo("Caliban");
sayHelloTwo("Miranda");
//-----------------------------

